def validate_name(name: str) -> str:
    """
    Функция, которая проверяет имя на различные ошибки
    и возвращает сообщение с ошибкой.
    """
    if not name:
        return 'Ошибка: пустое имя. '

    if len(name) < 3:
        return 'Ошибка: в имени не может быть меньше 3 символов. '

    n = 0
    for symbol in name:
        if symbol == ' ':
            n += 1

    if n > 1:
        return 'Ошибка: в имени допускается только 1 пробел. '

    return ''


def validate_age(age: int) -> str:
    """
    Функция, которая проверяет возраст на различные ошибки
    и возвращает сообщение с ошибкой.
    """
    if age <= 0:
        return 'Ошибка: ты еще не родился.'

    if age < 14:
        return 'Ошибка: тебе нету 14 лет.'
    return ''


def passport_advice(age: int) -> str:
    """
    Функция, которая возвращает совет, если пользователь
    достиг определенного возраста.
    """
    if 16 <= age <= 17:
        return f' Не забудь получить ваш первый паспорт!'
    elif 25 <= age <= 26:
        return f' Не забудь заменить паспорт по достижении 25 лет!'
    elif 45 <= age <= 46:
        return f' Не забудь заменить паспорт по достижении 45 лет!'
    return ''


