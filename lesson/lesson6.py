def main_func():
    """
    Функция main(), которая требует ввода данных, проверяет их,
    выводит ошибки на экран и возвращает сообщение.
    """
    while True:
        name = input('Введите ваше имя: ')
        age = int(input('Введите ваш возраст: '))

        error = ''
        error += validate_name(name)
        error += validate_age(age)

        if error:
            print(error)
        continue

        break

    print(f'Привет, {name.capitalize()}! Вам {age} лет! {passport_advice(age)}')


if __name__ == '__main__':
    main_func()
